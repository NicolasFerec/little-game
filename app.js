io.on('connection', function(socket){

	//io.emit('connect', {'id': socket.id});

	console.log('New player: '+socket.id);
	var player = new Player(socket.id);
	world.players.push(player);
	socket.emit('map', world.map);
	socket.emit('players', world.players);

	var gameLoop = setInterval(function(){
		socket.emit('players', world.players);
	}, 33);

	socket.on('moveUp', function(socket){
		player.moveUp();
	});

	socket.on('moveDown', function(socket){
		player.moveDown();
	});

	socket.on('moveLeft', function(socket){
		player.moveLeft();
	});

	socket.on('moveRight', function(socket){
		player.moveRight();
	});

	socket.on('disconnect', function(socket) {
		console.log('disconnect');
		world.players.splice(world.players.findIndex(function(element){
			return element.id == socket.id;
		}), 1);
		clearInterval(gameLoop);
	});

});


var Player = function(id){
	this.id = id;
	this.x = 0;
	this.y = 0;
}

Player.prototype.moveUp = function(){
	if(this.y > 0)
		this.y--;
};

Player.prototype.moveDown = function(){
	if(this.y < world.map.length - 1)
		this.y++;
};

Player.prototype.moveLeft = function(){
	if(this.x > 0)
		this.x--;
};

Player.prototype.moveRight = function(){
	if(this.x < world.map[0].length - 1)
		this.x++;
};
